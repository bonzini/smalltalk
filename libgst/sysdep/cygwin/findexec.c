#include "gstpriv.h"

#ifdef WIN32
# define WIN32_LEAN_AND_MEAN /* avoid including junk */
# include <windows.h>
# include <sys/cygwin.h>
#endif

/* The path to the executable, derived from argv[0].  */
const char *_gst_executable_path = NULL;

/* Store the full pathname of the current executable.  */
void
_gst_set_executable_path (const char *argv0)
{
  char location[MAX_PATH];
  static char location_as_posix_path[2 * MAX_PATH];
  int length = GetModuleFileName (NULL, location, sizeof (location));
  if (length <= 0)
    return NULL;

  /* On Cygwin, we need to convert paths coming from Win32 system calls
     to the Unix-like slashified notation.

     cygwin_conv_path returns error code including size is less than required
     See cygwin-api/func-cygwin-conv-path.html.  */
  if (cygwin_conv_path (CCP_WIN_A_TO_POSIX, location, location_as_posix_path, 2 * MAX_PATH))
    /* A sign of buffer overflow or path truncation.  */
    _gst_executable_path = NULL;
  else
    _gst_executable_path = _gst_get_full_file_name (location_as_posix_path);
}
